$(document).ready(function() {
  // start

  $('.header_link').mouseenter(function() {
    $(this).css('color', 'red')
  });


  $('.header_link').mouseleave(function() {
    $(this).css('color', '#FFEB3B')
  });
  // section
  $('.img_link').mouseenter(function() {
    $(this).css('color', 'red')
  });


  $('.img_link').mouseleave(function() {
    $(this).css('color', '#c1b54a')
  });
  // section
  $('.proj_link').mouseenter(function() {
    $(this).css('color', 'red')
  });


  $('.proj_link').mouseleave(function() {
    $(this).css('color', 'yellow')
  });







  // finish
})
